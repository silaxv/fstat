package data.entities;

/*
    TABLE tags
        id INT
        name VARCHAR(64)
*/

public class Tag {

    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
